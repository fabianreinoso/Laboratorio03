//Incremento y decremento de variables
let a = 3, b = 2;
let z = a + b;

//Incremento
//Pre-Incremento(El operador ++ antes de la variable)
// Incremento un valor, de 3 pasa a 4.
z = ++a;
console.log(a);
console.log(z);

//Post-Incremento(El operador ++ despues de la variable)

z = b++;
console.log(b);
console.log(z);