//Operadores Aritmeticos

// Suma dos numeros
const a = 3, b = 2;
let z = a + b;
console.log("El Resultado de la suma es: " + z);

//Resta de dos numeros, variables del primer ejemplo
z = a - b;
console.log("El Resultado  de la resta es: " + z);

// Multiplicacion
z = a * b;
console.log("El Resultado de la multiplicacion es: " + z)

// Division

z = a / b;
console.log("El resultado de la divison es: " + z);

//Residuo o modulo
z = a % b;
console.log("El resultado del residuo es: " , z );

// Potencia o exponente
z = a ** b;
console.log("El resutlado de operador exponente es: " + z);
